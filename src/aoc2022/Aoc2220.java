  public static void main(String[] args) throws Exception {

    for (int i = 0; i < (DEBUG ? tests().length : 1); i++) {
      List<Num> l = new LinkedList<>();
      List<Num> order = new ArrayList<>();
      Num z = new Num(0);

      List<Integer> in = new ArrayList<>();
      if (DEBUG) {
        for (var tt : tests()[i][0]) {
          in.add(tt);
        }
      } else {
        in = Parse.integers(getInput());
      }

      for (var num : in) {
        var n = new Num(num);
        if (n.val == 0) {
          z = n;
        }
        l.add(n);
        order.add(n);
      }

      int mod = l.size() - 1;

      for (var num : order) {
        if (num.val == 0) {
          continue;
        }
        int pos = l.indexOf(num) + mod * 2;
        int newPos = (pos + num.val) % (mod);
        l.remove(num);
        l.add(newPos, new Num(num.val));
      }

      if (DEBUG) {
        boolean ok = true;
        for (int j = 0; j < l.size(); j++) {
          var expected = tests()[i][1];
          if (l.get(j).val != expected[j]) {
            ok = false;
            System.out.println("Test num " + i + " failed at idx " + j + ", " + l.get(j).val + " expected to be " + expected[j]);
          }
        }
        if (ok) {
          System.out.printf(".");
        }
      }

      System.out.println(l.get((l.indexOf(z) + 1000) % mod).val
        + l.get((l.indexOf(z) + 2000) % mod).val
        + l.get((l.indexOf(z) + 3000) % mod).val);
    }
  }

